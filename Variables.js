// Variable = placeholder to star any value
// JavaScript uses 'var' for variables
// JavaScript like Kotlin has type inference
var firstName = "Stefan" // string
console.log(firstName)

var age = 27 // int
console.log(age)

var sex = "Male" // string
console.log(sex)

var isMale = true // boolean
console.log(isMale)

// We can store dates by using 'new Date()'
var birthday = new Date(1995, 0, 6)
console.log(birthday)

// We can also have objects
var dog = {} // object
console.log(dog)

// We can have empty variables too using 'undefined'.. 
// kind of like nullables in Kotlin
var empty = undefined
console.log(empty)