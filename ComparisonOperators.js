/**
 * These are the comparison operators that JavaScript has to offer.
 * 
 * We also go over logical operators towards the end of the file.
 */

// Comparison Operators
// < <= > >= == !=
// the return = a boolean is either true or false
console.log(10 == 10) // equal to
console.log(10 != 10) // not equal
console.log(10 < 10) // less
console.log(10 <= 10) // less than or equal to
console.log(10 > 10) // greater than
console.log(10 >= 10) // greater than or equal to

console.log("A" == "B") 
console.log("A" != "B")

// These are logical operators which evaluate logical expressions.
// allows us to connect two or more expressions
// Logical Operators
// && ||
console.log(10 == 10 && "A" == "A")
console.log(10 == 10 || "A" == "B")